var clima = 'quente';

clima = 'frio';

console.log(clima); // o valor alterou para frio, var é pouco usado



let tempo = 'ensolarado';

tempo = 'chuvoso';

console.log(tempo); // o valor alterou para chuvoso



const cor = 'preto';

cor = 'branco';

console.log(cor); // deu erro pois a variavel é constante e jamais sera alterada