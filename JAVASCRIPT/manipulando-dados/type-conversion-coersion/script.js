/*
    type conversion (typecasting) vs type coersion

    alteração de um tipo de dado para outro tipo

    typecasting 
        EU altero os tipos

    type coersion
        o JAVASCRIPT altera os tipos

*/

console.log('9' + 5); // type coersion, o 5 vira string, retornando 95

console.log(Number('9' + 5)); // EU transformo tudo em number, retornando 14

